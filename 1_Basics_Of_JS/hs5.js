// Booleans & Comparison Operator

let num1 = 5;
let num2 = 5;
let num3 = "5";
let num4 = 6

console.log(num1 > num2);  /* greater-than operator */
console.log(num1 < num2);  /* Less-than*/ 
console.log(num1 == num2); /* Equal-to */

// == vs ===

console.log(num1==num2); /* == check the value of data */
console.log(num1===num3); /* While === check the type of the data */

// != vs !===
console.log(num1 != num4 ); /* != it check only the values are not equal */
console.log(num1 !== num3 ); /* !== it check the types are not equal */